package com.bean;

public class FacultySignupBean 
{
	private int faculty_signup_id;
	private String name;
	private String user;
	private String pass;
	private String dob;
	private String country;
	private int mob;
	private String degree;
	private String subject;
	private String time;
	
	
	public int getFaculty_signup_id() 
	{
		return faculty_signup_id;
	}
	public void setFaculty_signup_id(int faculty_signup_id) 
	{
		this.faculty_signup_id = faculty_signup_id;
	}
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public String getUser() 
	{
		return user;
	}
	public void setUser(String user) 
	{
		this.user = user;
	}
	public String getPass() 
	{
		return pass;
	}
	public void setPass(String pass) 
	{
		this.pass = pass;
	}
	public String getDob() 
	{
		return dob;
	}
	public void setDob(String dob) 
	{
		this.dob = dob;
	}
	public String getCountry() 
	{
		return country;
	}
	public void setCountry(String country) 
	{
		this.country = country;
	}
	public int getMob() 
	{
		return mob;
	}
	public void setMob(int mob) 
	{
		this.mob = mob;
	}
	public String getDegree() 
	{
		return degree;
	}
	public void setDegree(String degree) 
	{
		this.degree = degree;
	}
	public String getSubject() 
	{
		return subject;
	}
	public void setSubject(String subject) 
	{
		this.subject = subject;
	}
	public String getTime() 
	{
		return time;
	}
	public void setTime(String time) 
	{
		this.time = time;
	}
}
